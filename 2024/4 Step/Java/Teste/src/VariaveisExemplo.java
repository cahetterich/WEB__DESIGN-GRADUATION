public class VariaveisExemplo {
    public static void main(String[] args) {

        //Tipos primitivos
        // byte, short, int, long (numeros inteiros)
        int idade;
        idade = 22;

        // float, double (precisão maior) (numeros fracionarios)
        float altura = 1.80f; //(numero tipo f)

        // char (alfanumerico)
        char genero = 'M';

        // boolean (true o false)
        boolean ehEstudante = true;

        //Tipo de referência
        // String (texto)
        String nome = "Carla Hetterich";

        // Exibindo os valores das variáveis
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Gênero: " + genero);
        System.out.println("É estudante? " + ehEstudante);
    }
}
