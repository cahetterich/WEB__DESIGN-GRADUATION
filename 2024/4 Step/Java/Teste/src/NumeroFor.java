public class NumeroFor {
    public static void main(String[] args) {
        System.out.println("Contagem Regressiva:");

        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }

        System.out.println("Fim da contagem regressiva!");
    }
}

